#include "libft.h"

size_t		ft_wcount(const char *s, char c)
{
	unsigned int	i;
	size_t			w;

	i = 0;
	w = 0;
	while (s[i])
	{
		while (s[i] == c && s[i])
			i++;
		if (s[i])
			w++;
		while (s[i] != '\0' && (s[i] != c))
			i++;
	}
	return (w);
}
