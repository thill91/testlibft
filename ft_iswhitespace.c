#include "libft.h"

int		ft_iswhitespace(const char *s, int i)
{
	if (s[i] == ' ' || s[i] == '\n' || s[i] == '\t' ||
		s[i] == '\f' || s[i] == '\r' || s[i] == '\f')
		return (1);
	else
		return (0);
}
