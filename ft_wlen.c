#include "libft.h"

size_t		ft_wlen(const char *s, char c)
{
	unsigned int	i;
	size_t			len;

	i = 0;
	len = 0;
	while (s[i] == c && s[i])
		i++;
	while (s[i] != c && s != '\0')
	{
		i++;
		len++;
	}
	return (len);
}
